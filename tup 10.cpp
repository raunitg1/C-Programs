//While Loop
#include<iostream>
using namespace std;
int main(){
    int i=1;
    while (i<=10) //This is fininte Loop. Only enter the loop for i<=10
    {
        cout<<i<<endl;
        i++;

    }
    while (true) //This infinite Loop. Condtion will be always true and enter the loop.
    {
        cout<<i<<endl; 
        i++;
    }
     
    
}