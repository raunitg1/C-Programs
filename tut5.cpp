#include<iostream>
using namespace std;

int main(){
    int num1,num2;
    //cout is called Insertion Operator. cout is used to show output of the program.
    //cin is called Extraction Operator. cin is used to take input from the user.
    cout<<"Enter the Value of num 1:\n";
    cin>>num1;
    cout<<"Enter the Value of num 2:\n";
    cin>>num2;
    cout<<"The Sum of num 1 and num 2 is: "<<num1+num2;

    return 0;
}